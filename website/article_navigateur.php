	<h3> <strong> Alternative à Internet Explorer </strong> </h3>
	<p>
		Internet explorer, le navigateur par défaut de Windows n'est pas le seul navigateur.
	</p>
	
	<h4> Mozilla Firefox </h4>
	<p>
		<a href="http://www.mozilla.org/fr/firefox/new/"> Mozilla Firefox </a> est un navigateur web libre (open source) et gratuit. Il est très modulable en fonction de l'envie de l'utlisateur grâce à des centaines d'extensions et de thèmes graphiques.
		<br>
		Les fonctionnalités présentes par défaut dans Firefox incluent notamment:
		<ul>
		<li>la navigation par onglets (facilement réorganisables)</li>
		<li>une page d'accueil qui contient un champ de recherche Google, ainsi que des raccourcis vers les téléchargements, les marque-pages, l'historique de navigation, les modules complémentaires, la synchronisation et les paramètres</li>
		<li>un bloqueur de fenêtres intruses (appelées aussi pop-ups)</li>
		<li>un système de moteurs de recherche intégré et facilement personnalisable</li>
		<li>les marque-pages (appelés aussi applisignets ou encore favelets)</li>
		<li>les marque-pages dynamiques (aussi appelés « signets ») dynamiques (lecteur de flux d’informations aux formats RSS et Atom)</li>
		<li>un gestionnaire de téléchargement</li>
		<li>un correcteur orthographique intégré ; un filtre anti-hameçonnage (filoutage ou phishing)</li>
		<li>un mode de navigation privée, où Firefox ne conserve aucune donnée sur les sites et pages visités</li>
		<li>compatibilité IPv6</li>
		<li>affichage des images translucides au format PNG</li>
		<li>la navigation avec géolocalisation intégrée</li>
		<li>un lecteur de PDF intégré</li>
	</p>
	
