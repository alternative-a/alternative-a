		<img src="images/gimp_screenshot_windows.jpg" alt="gimp" title="Design de Gimp" />
	</p>
	
	<h4> Paint </h4>
	<p>
		<a href="http://windows.microsoft.com/fr-fr/windows7/products/features/paint">Paint</a> est un logiciel livré avec toutes les versions de Windows. Il vous permet de faire des modifications très basiques sur vos images facilement.
		<br>
		Ces principales options dans la barre d'outils:
		<ul>
		<li>sélection libre</li>
		<li>sélection rectangulaire</li>
		<li>Redimensionner et incliner</li>
		<li>gomme/gomme couleur</li>
		<li>remplissage</li>
		<li>prélèvement d'une couleur</li>
		<li>zoom/Loupe</li>
		<li>pinceau</li>
		<li>brosse</li>
		<li>aérographe</li>
		<li>Pastel</li>
		<li>texte</li>
		<li>ligne</li>
		<li>courbe</li>
		<li>rectangle</li>
		<li>polygone</li>
		<li>ellipse</li>
		<li>rectangle arrondi et autres formes géométriques</li>
		</ul>
	</p>
	
