	<h3> <strong> Alternative à Microsoft Office Outlook </strong> </h3>
	<p>
		<a href="http://office.microsoft.com/fr-fr/outlook/">Microsoft Outlook<a/> est un gestionnaire d'informations personnelles et un client de courrier électronique propriétaire édité par Microsoft. Il fait partie de la suite bureautique Microsoft Office.
		<br>
		Bien qu'il soit principalement utilisé en tant qu'application de courrier électronique, il propose aussi un calendrier et un gestionnaire de tâche et de contact.
	</p>

	<h4> Mozilla Thunderbird </h4>
	<p>
		<a href="http://www.mozilla.org/fr/thunderbird/">Thunderbird</a> est un client de messagerie libre distribué gratuitement par la Fondation Mozilla.
	</p>

	<h4> Opéra Mail </h4>
	<p>
		<a href="http://www.opera.com/fr/computer/mail">Opéra Mail</a> est un client de messagerie développé par Opéra Software.
	</p>
